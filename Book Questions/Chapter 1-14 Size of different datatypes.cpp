#include<iostream>
using namespace std;
int main()
{
	int x;
	float y;
	double z;
	char a;
	cout<<"Size of an Integer : "<<sizeof(x)<<endl;
	cout<<"Size of an Float : "<<sizeof(y)<<endl;	
	cout<<"Size of an Double : "<<sizeof(z)<<endl;
	cout<<"Size of an Character : "<<sizeof(a)<<endl;
	return 0;
}
